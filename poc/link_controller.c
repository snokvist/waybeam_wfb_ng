/*
 * link_controller — merged adaptive controller for the wfb-ng POC.
 *
 * Combines fec_controller (FEC k/n sizing from venc sidecar + tx stats) and
 * mcs_selector (MCS bucket selection from rx_ant RSSI + loss) into a single
 * binary. Both control loops live in the same poll() event loop, share the
 * wfb_tx control socket, share a RadioState snapshot, and feed a unified
 * REST + SSE + WebUI on a single port (default 8765).
 *
 * Coordination:
 *   - mcs subsystem writes CMD_SET_RADIO (mcs_index only, every other field
 *     preserved from a startup CMD_GET_RADIO sync).
 *   - fec subsystem writes CMD_SET_FEC and asserts venc bitrate via HTTP.
 *   - When mcs commits a SET_RADIO, fec is explicitly armed (controller_arm_
 *     settle) so the EWMA can converge before fec re-emits — replaces the
 *     fec_controller's old detect-via-tx_stats round-trip.
 *
 * REST / WebUI on :8765
 *   GET /                 → embedded HTML WebUI
 *   GET /help             → plain-text route + tunable help (curl-friendly)
 *   GET /params           → JSON of all tunables (dotted keys)
 *   GET /schema           → JSON array of tunable metadata
 *   GET /set?k=v&k=v      → multi-namespace write
 *   GET /status           → unified JSON snapshot
 *   GET /fec/status       → fec-only subset
 *   GET /mcs/status       → mcs-only subset
 *   GET /events           → text/event-stream — every log line as JSON
 *   GET /health           → "ok\n"
 *
 * Single thread, poll() loop. No libs beyond libc.
 *
 * Target: SigmaStar Infinity6E (armv7l / OpenIPC). Cross-build with the
 * star6e toolchain. Designed to run on the vehicle alongside venc + wfb_tx.
 */

