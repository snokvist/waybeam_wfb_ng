static double tunable_read(const TunableDesc *t, const Config *c)
{
	const char *base = (const char *)c + t->offset;
	switch (t->type) {
	case TF_INT:   return (double) *(const int *)base;
	case TF_LONG:  return (double) *(const long *)base;
	case TF_FLOAT: return (double) *(const float *)base;
	case TF_BOOL:  return (double) (*(const bool *)base ? 1 : 0);
	}
	return 0.0;
}

/* Strict parser: reject garbage tokens (atoi/atof silently return 0 on
 * "abc", which would corrupt the field with a value the operator did not
 * intend). For numeric types we use strtoX with end-pointer check; only
 * trailing whitespace is tolerated. For booleans we whitelist the common
 * spellings. Returns 0 ok, -1 on parse / range failure. */
static int parse_strict_double(const char *s, double *out)
{
	if (!s || !*s) return -1;
	char *end;
	double v = strtod(s, &end);
	if (end == s) return -1;
	while (*end == ' ' || *end == '\t' || *end == '\r' || *end == '\n') end++;
	if (*end) return -1;
	*out = v;
	return 0;
}
static int parse_strict_long(const char *s, long *out)
{
	if (!s || !*s) return -1;
	char *end;
	long v = strtol(s, &end, 10);
	if (end == s) return -1;
	while (*end == ' ' || *end == '\t' || *end == '\r' || *end == '\n') end++;
	if (*end) return -1;
	*out = v;
	return 0;
}
static int parse_strict_bool(const char *s, bool *out)
{
	if (!s || !*s) return -1;
	if (!strcasecmp(s, "1") || !strcasecmp(s, "true") ||
	    !strcasecmp(s, "yes") || !strcasecmp(s, "on")) {
		*out = true;  return 0;
	}
	if (!strcasecmp(s, "0") || !strcasecmp(s, "false") ||
	    !strcasecmp(s, "no") || !strcasecmp(s, "off")) {
		*out = false; return 0;
	}
	return -1;
}

static int tunable_write(const TunableDesc *t, Config *c, const char *val)
{
	char *base = (char *)c + t->offset;
	switch (t->type) {
	case TF_INT: {
		long v;
		if (parse_strict_long(val, &v) != 0) return -1;
		if (t->lo != t->hi && (v < t->lo || v > t->hi)) return -1;
		if (v < INT_MIN || v > INT_MAX) return -1;
		*(int *)base = (int)v;
		return 0;
	}
	case TF_LONG: {
		long v;
		if (parse_strict_long(val, &v) != 0) return -1;
		if (t->lo != t->hi && ((double)v < t->lo || (double)v > t->hi)) return -1;
		*(long *)base = v;
		return 0;
	}
	case TF_FLOAT: {
		double v;
		if (parse_strict_double(val, &v) != 0) return -1;
		if (t->lo != t->hi && (v < t->lo || v > t->hi)) return -1;
		*(float *)base = (float)v;
		return 0;
	}
	case TF_BOOL: {
		bool b;
		if (parse_strict_bool(val, &b) != 0) return -1;
		*(bool *)base = b;
		return 0;
	}
	}
	return -1;
}

static const TunableDesc *tunable_find(const char *name)
{
	for (size_t i = 0; i < TUNABLES_COUNT; i++)
		if (strcmp(TUNABLES[i].name, name) == 0) return &TUNABLES[i];
	return NULL;
}

static int json_append_field(char *buf, size_t cap, size_t pos,
                             const TunableDesc *t, const Config *c, bool first)
{
	double v = tunable_read(t, c);
	const char *sep = first ? "" : ",";
	int n;
	if (t->type == TF_INT || t->type == TF_LONG)
		n = snprintf(buf + pos, cap - pos, "%s\"%s\":%lld",
		             sep, t->name, (long long)v);
	else if (t->type == TF_BOOL)
		n = snprintf(buf + pos, cap - pos, "%s\"%s\":%s",
		             sep, t->name, v != 0.0 ? "true" : "false");
	else
		n = snprintf(buf + pos, cap - pos, "%s\"%s\":%g",
		             sep, t->name, v);
	if (n < 0 || (size_t)n >= cap - pos) return -1;
	return n;
}

static int params_to_json(const Config *c, char *buf, size_t cap)
{
	size_t pos = 0;
	int n = snprintf(buf + pos, cap - pos, "{");
	if (n < 0) return -1;
	pos += n;
	for (size_t i = 0; i < TUNABLES_COUNT; i++) {
		int w = json_append_field(buf, cap, pos, &TUNABLES[i], c, i == 0);
		if (w < 0) return -1;
		pos += w;
	}
	n = snprintf(buf + pos, cap - pos, "}");
	if (n < 0 || (size_t)n >= cap - pos) return -1;
	pos += n;
	return (int)pos;
}

/* Schema describes each tunable with type/bounds/help so the WebUI can
 * render an input form without hard-coding field metadata. JSON array. */
