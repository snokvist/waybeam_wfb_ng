/* Redundancy curve (k → r, linear interp). */
typedef struct { int k; float r; } RedundancyPoint;
static const RedundancyPoint REDUNDANCY_CURVE[] = {
	{  1, 0.50f }, {  4, 0.40f }, {  8, 0.33f },
	{ 16, 0.30f }, { 32, 0.27f }, { 48, 0.25f },
};
static const int REDUNDANCY_CURVE_LEN =
	sizeof(REDUNDANCY_CURVE) / sizeof(REDUNDANCY_CURVE[0]);

static float interpolate_redundancy(int k)
{
	if (k <= REDUNDANCY_CURVE[0].k) return REDUNDANCY_CURVE[0].r;
	if (k >= REDUNDANCY_CURVE[REDUNDANCY_CURVE_LEN - 1].k)
		return REDUNDANCY_CURVE[REDUNDANCY_CURVE_LEN - 1].r;
	for (int i = 0; i < REDUNDANCY_CURVE_LEN - 1; i++) {
		int k0 = REDUNDANCY_CURVE[i].k;
		int k1 = REDUNDANCY_CURVE[i + 1].k;
		if (k >= k0 && k <= k1) {
			float t = (float)(k - k0) / (float)(k1 - k0);
			return REDUNDANCY_CURVE[i].r +
			       t * (REDUNDANCY_CURVE[i + 1].r - REDUNDANCY_CURVE[i].r);
		}
	}
	return REDUNDANCY_CURVE[REDUNDANCY_CURVE_LEN - 1].r;
}

/* Headroom ring (rolling max/avg of frame sizes). */
#define RING_MAX 512
typedef struct {
	uint64_t ts_us[RING_MAX];
	uint32_t size[RING_MAX];
	int      head;
	int      count;
} HeadroomRing;

static void ring_push(HeadroomRing *r, uint64_t ts, uint32_t sz)
{
	r->ts_us[r->head] = ts;
	r->size[r->head] = sz;
	r->head = (r->head + 1) % RING_MAX;
	if (r->count < RING_MAX) r->count++;
}

static void ring_expire(HeadroomRing *r, uint64_t cutoff_us)
{
	while (r->count > 0) {
		int oldest = (r->head - r->count + RING_MAX) % RING_MAX;
		if (r->ts_us[oldest] >= cutoff_us) break;
		r->count--;
	}
}

static float compute_headroom(const HeadroomRing *r,
                              float margin, float lo, float hi)
{
	if (r->count < 2) return lo;
	uint64_t sum = 0;
	uint32_t mx = 0;
	for (int i = 0; i < r->count; i++) {
		int idx = (r->head - 1 - i + RING_MAX) % RING_MAX;
		uint32_t v = r->size[idx];
		sum += v;
		if (v > mx) mx = v;
	}
	float avg = (float)sum / (float)r->count;
	if (avg <= 0.0f) return lo;
	float ratio = (float)mx / avg * margin;
	if (ratio < lo) ratio = lo;
	if (ratio > hi) ratio = hi;
	return ratio;
}

typedef struct {
	int   k, n;
	float redundancy;
	int   packets_per_frame;
	float avg_frame_size;
	float headroom;
} FecParams;

static FecParams fec_compute(float avg_size, float headroom, const Config *cfg,
                             int current_ppf)
{
	FecParams p = {0};
	float target = avg_size * headroom;
	int ppf = (int)ceilf(target / (float)cfg->fec.mtu);
	if (ppf < 1) ppf = 1;

	if (current_ppf > 0 && cfg->fec.ppf_deadband_frac > 0.0f) {
		float slack = (float)cfg->fec.mtu * cfg->fec.ppf_deadband_frac;
		float up_edge   = (float)(current_ppf + 1) * (float)cfg->fec.mtu + slack;
		float down_edge = (float)(current_ppf - 1) * (float)cfg->fec.mtu - slack;
		if (ppf > current_ppf && target < up_edge)        ppf = current_ppf;
		else if (ppf < current_ppf && target > down_edge) ppf = current_ppf;
	}

	int k = ppf;
	if (k < cfg->fec.min_k) k = cfg->fec.min_k;
	if (k > cfg->fec.max_k) k = cfg->fec.max_k;

	float r = interpolate_redundancy(k);
	if (r > 0.99f) r = 0.99f;
	int n = (int)ceilf((float)k / (1.0f - r));
	if (n < cfg->fec.min_n) n = cfg->fec.min_n;
	if (n > cfg->fec.max_n) n = cfg->fec.max_n;
	if (n <= k) n = k + 1;

	p.k = k;
	p.n = n;
	p.redundancy = r;
	p.packets_per_frame = ppf;
	p.avg_frame_size = avg_size;
	p.headroom = headroom;
	return p;
}

typedef struct {
	float     avg_frame_size;
	FecParams current;
	bool      have_current;
	uint64_t  last_update_us;
	uint64_t  start_us;
	uint32_t  update_count;
	uint64_t  boost_until_us;
	bool      was_boosting;
	uint64_t  bitrate_grace_until_us;
	bool      was_in_grace;
	uint64_t  k_down_pending_since_us;
	int       k_down_pending_target;
	uint64_t  k_up_pending_since_us;
	int       k_up_pending_target;
} Controller;

/* Arm the post-change settling window (extending only). */
static void controller_arm_settle(Controller *c, uint64_t now, float secs)
{
	uint64_t new_end = now + (uint64_t)(secs * 1e6f);
	if (new_end > c->bitrate_grace_until_us)
		c->bitrate_grace_until_us = new_end;
}

static bool controller_commit(Controller *c, const FecParams *cand,
                              uint64_t now, FecParams *out)
{
	c->current = *cand;
	c->last_update_us = now;
	c->update_count++;
	c->k_down_pending_since_us = 0;
	c->k_down_pending_target = 0;
	c->k_up_pending_since_us = 0;
	c->k_up_pending_target = 0;
	*out = *cand;
	return true;
}

static void controller_arm_boost(Controller *c, const Config *cfg, uint64_t now)
{
	if (cfg->fec.boost_s > 0.0f)
		c->boost_until_us = now + (uint64_t)(cfg->fec.boost_s * 1e6f);
}

static bool controller_update(Controller *c, const Config *cfg,
                              uint32_t frame_size, HeadroomRing *ring,
                              uint64_t now, FecParams *out)
{
	if (c->start_us == 0) c->start_us = now;

	ring_push(ring, now, frame_size);
	uint64_t cutoff = now - (uint64_t)(cfg->fec.headroom_window_s * 1e6);
	ring_expire(ring, cutoff);

	if (c->avg_frame_size <= 0.0f) {
		c->avg_frame_size = (float)frame_size;
	} else {
		c->avg_frame_size =
			cfg->fec.ewma_alpha * (float)frame_size +
			(1.0f - cfg->fec.ewma_alpha) * c->avg_frame_size;
	}

	float headroom = compute_headroom(
		ring, cfg->fec.headroom_margin, cfg->fec.headroom_min, cfg->fec.headroom_max);

	int cur_ppf = c->have_current ? c->current.packets_per_frame : 0;
	FecParams cand = fec_compute(c->avg_frame_size, headroom, cfg, cur_ppf);

	bool boost_active = (c->boost_until_us != 0 && now < c->boost_until_us);
	bool boost_expired_now = (c->boost_until_us != 0 && !boost_active);
	if (boost_expired_now) c->boost_until_us = 0;
	bool boost_entry = (boost_active && !c->was_boosting);
	c->was_boosting = boost_active;

	if (boost_active) {
		int parity = cand.n - cand.k;
		if (parity < 1) parity = 1;
		int boosted = cand.k + (int)ceilf((float)parity * cfg->fec.boost_mult);
		if (boosted > cfg->fec.max_n) boosted = cfg->fec.max_n;
		if (boosted <= cand.k)        boosted = cand.k + 1;
		cand.n = boosted;
		cand.redundancy = 1.0f - (float)cand.k / (float)cand.n;
	}

	bool in_startup_grace = (now - c->start_us) <
	                        (uint64_t)(cfg->fec.startup_grace_s * 1e6f);
	bool in_settle_grace  = (c->bitrate_grace_until_us != 0 &&
	                         now < c->bitrate_grace_until_us);
	bool in_grace = in_startup_grace || in_settle_grace;
	bool settle_just_ended = c->was_in_grace && !in_settle_grace;
	c->was_in_grace = in_settle_grace;

	if (!c->have_current) {
		if (in_grace) return false;
		c->have_current = true;
		return controller_commit(c, &cand, now, out);
	}

	if (boost_entry || boost_expired_now) {
		if (cand.k == c->current.k && cand.n == c->current.n) return false;
		return controller_commit(c, &cand, now, out);
	}

	if (in_grace) return false;

	if (settle_just_ended) {
		if (cand.k == c->current.k && cand.n == c->current.n) return false;
		return controller_commit(c, &cand, now, out);
	}

	int k_delta = cand.k - c->current.k;

	if (k_delta < 0) {
		c->k_up_pending_since_us = 0;
		c->k_up_pending_target = 0;
		if (c->k_down_pending_since_us == 0)
			c->k_down_pending_since_us = now;
		c->k_down_pending_target = cand.k;
		float held_s = (float)(now - c->k_down_pending_since_us) / 1e6f;
		if (held_s < cfg->fec.k_down_dwell_s) return false;
		return controller_commit(c, &cand, now, out);
	}

	c->k_down_pending_since_us = 0;
	c->k_down_pending_target = 0;

	if (k_delta == 0) {
		c->k_up_pending_since_us = 0;
		c->k_up_pending_target = 0;
		return false;
	}

	if (c->k_up_pending_since_us == 0)
		c->k_up_pending_since_us = now;
	c->k_up_pending_target = cand.k;
	float up_held_s = (float)(now - c->k_up_pending_since_us) / 1e6f;
	if (up_held_s < cfg->fec.k_up_dwell_s) return false;

	if (k_delta < cfg->fec.k_hyst_up) return false;
	float elapsed = (float)(now - c->last_update_us) / 1e6f;
	if (elapsed < cfg->fec.cooldown_up_s) return false;

	return controller_commit(c, &cand, now, out);
}

/* ── FPS estimator ───────────────────────────────────────────────────── */

typedef struct {
	uint64_t prev_ready_us;
	float    ewma_period_us;
	bool     have_prev;
} FpsEst;

static void fps_feed(FpsEst *f, uint64_t ready_us)
{
	if (!f->have_prev) {
		f->prev_ready_us = ready_us;
		f->have_prev = true;
		return;
	}
	uint64_t dt = ready_us - f->prev_ready_us;
	if (dt == 0 || dt > 1000000ULL) {
		f->prev_ready_us = ready_us;
		return;
	}
	if (f->ewma_period_us <= 0.0f) {
		f->ewma_period_us = (float)dt;
	} else {
		f->ewma_period_us = 0.1f * (float)dt + 0.9f * f->ewma_period_us;
	}
	f->prev_ready_us = ready_us;
}

static float fps_get(const FpsEst *f)
{
	if (f->ewma_period_us <= 0.0f) return 0.0f;
	return 1e6f / f->ewma_period_us;
}

/* ── Shared RadioState ───────────────────────────────────────────────── */

/* HT MCS 0..7 PHY rate (Mbps), BW20 LGI. */
static const float HT_20L[8] = {
	6.5f, 13.0f, 19.5f, 26.0f, 39.0f, 52.0f, 58.5f, 65.0f
};
static const float VHT_20L_EXT[10] = {
	6.5f, 13.0f, 19.5f, 26.0f, 39.0f, 52.0f, 58.5f, 65.0f,
	78.0f, 86.7f
};

typedef struct {
	bool  valid;
	int   short_gi, bandwidth, mcs, vht_mode, vht_nss, stbc, ldpc;
	float phy_mbps;
} RadioState;

static float phy_mbps(int mcs, int bw_mhz, int short_gi,
                      int vht_mode, int vht_nss)
{
	int idx_max = vht_mode ? 9 : 7;
	if (mcs < 0) mcs = 0;
	if (mcs > idx_max) mcs = idx_max;
	float base = vht_mode ? VHT_20L_EXT[mcs] : HT_20L[mcs];
	float bw_scale;
	switch (bw_mhz) {
	case 40:  bw_scale = 2.1f; break;
	case 80:  bw_scale = 4.5f; break;
	case 160: bw_scale = 9.0f; break;
	default:  bw_scale = 1.0f; break;
	}
	float gi_scale = short_gi ? (10.0f / 9.0f) : 1.0f;
	int nss = (vht_mode && vht_nss > 0) ? vht_nss : 1;
	return base * bw_scale * gi_scale * (float)nss;
}

/* Theoretical airtime / packet-rate ceiling for a single PHY operating
 * point. Bootstrap model only — operator-measured ceilings should
 * override via the live tunables (fec.airtime_util, fec.pkt_overhead_us,
 * fec.pkt_overhead_bytes). Returns 0 if any input is degenerate.
 *
 *   airtime_us = pkt_overhead_us + (payload + pkt_overhead_bytes)*8 / phy_mbps
 *   safe_pps   = airtime_util * 1e6 / airtime_us
 */
static float safe_pps(int payload_bytes, float phy_mbps_,
                      float pkt_overhead_us, int pkt_overhead_bytes,
                      float airtime_util)
{
	if (phy_mbps_ <= 0.0f || payload_bytes <= 0 || airtime_util <= 0.0f)
		return 0.0f;
	float airtime_us = pkt_overhead_us +
	    (float)(payload_bytes + pkt_overhead_bytes) * 8.0f / phy_mbps_;
	if (airtime_us <= 0.0f) return 0.0f;
	return airtime_util * 1.0e6f / airtime_us;
}

/* Apply a fresh radio observation: update RadioState, detect external
 * changes vs the previous snapshot, log them, arm fec settle window
 * (mcs_settle_s) on any change, arm parity boost on MCS-down. */
static void radio_apply_observation(RadioState *radio, Controller *ctrl,
                                    const Config *cfg,
                                    int new_mcs, int new_bw, int new_gi,
                                    int new_stbc, int new_ldpc,
                                    int new_vht_mode, int new_vht_nss,
                                    uint64_t now,
                                    bool from_self)
{
	bool was_valid = radio->valid;
	int  prev_mcs  = radio->mcs;
	int  prev_bw   = radio->bandwidth;
	int  prev_gi   = radio->short_gi;
	int  prev_vht  = radio->vht_mode;
	int  prev_nss  = radio->vht_nss;

	radio->valid     = true;
	radio->stbc      = new_stbc;
	radio->ldpc      = new_ldpc;
	radio->short_gi  = new_gi;
	radio->bandwidth = new_bw;
	radio->mcs       = new_mcs;
	radio->vht_mode  = new_vht_mode;
	radio->vht_nss   = new_vht_nss;
	radio->phy_mbps  = phy_mbps(radio->mcs, radio->bandwidth,
	                            radio->short_gi, radio->vht_mode,
	                            radio->vht_nss);

	if (was_valid) {
		bool mcs_changed = (radio->mcs != prev_mcs);
		bool any_changed = mcs_changed
		    || radio->bandwidth != prev_bw
		    || radio->short_gi  != prev_gi
		    || radio->vht_mode  != prev_vht
		    || radio->vht_nss   != prev_nss;
		if (any_changed) {
			/* Skip the "external" log when the change was driven by our
			 * own mcs subsystem — caller has already logged the transition
			 * with full context. The settle/boost arm still fire below. */
			if (!from_self) {
				LOG_FEC("radio: external change mcs %d->%d bw %d->%d gi %d->%d vht %d->%d nss %d->%d (phy=%.1fMbps)",
				    prev_mcs, radio->mcs,
				    prev_bw, radio->bandwidth,
				    prev_gi, radio->short_gi,
				    prev_vht, radio->vht_mode,
				    prev_nss, radio->vht_nss,
				    radio->phy_mbps);
			}
			if (cfg->fec.enabled) {
				controller_arm_settle(ctrl, now, cfg->fec.mcs_settle_s);
				if (mcs_changed && radio->mcs < prev_mcs) {
					controller_arm_boost(ctrl, cfg, now);
					if (cfg->fec.boost_s > 0.0f)
						LOG_FEC("parity boost armed for %.1fs (mult=%.2f) [MCS drop %d->%d]",
						    cfg->fec.boost_s, cfg->fec.boost_mult,
						    prev_mcs, radio->mcs);
				}
			}
		}
	}
}

/* External-FEC tracker: separated from radio_apply_observation because
 * only the wfb_stats datagram path carries fec_k/fec_n; the polling and
 * MCS-self-mirror callers always passed -1, -1. Keep the bookkeeping
 * orthogonal so radio mirroring can have a smaller signature.
 *
 * obs_k / obs_n: most recently observed sizes from the producer.
 * last_written_*: in/out — controller's last-known authoritative pair.
 * Logs once per drift, then updates last_written so the next divergence
 * has to be a fresh change. */
static void radio_track_external_fec(int obs_k, int obs_n,
                                     int *last_written_fec_k,
                                     int *last_written_fec_n)
{
	if (obs_k <= 0 || obs_n <= 0) return;
	if (*last_written_fec_k > 0 && *last_written_fec_n > 0 &&
	    (obs_k != *last_written_fec_k ||
	     obs_n != *last_written_fec_n))
	{
		LOG_FEC("external change observed k=%d n=%d (controller last wrote k=%d n=%d)",
		    obs_k, obs_n,
		    *last_written_fec_k, *last_written_fec_n);
	}
	*last_written_fec_k = obs_k;
	*last_written_fec_n = obs_n;
}

/* Forward decl. */
static int venc_apply(const Config *cfg, long bitrate_kbps, int payload_bytes);

/* Compute the payload size to ask venc for, given the current target
 * bitrate and PHY rate. Uses the largest-OK rule: payload_max minimises
 * pps for any fixed bitrate, so prefer it whenever the airtime model
 * says the link can sustain the target bitrate at that payload. If even
 * the ceiling is too tight, return the ceiling anyway (still the lowest
 * pps) and surface insufficient=true so the operator sees the warning.
 *
 * Returns the chosen payload (always between payload_min and payload_max
 * when feature is enabled) or 0 when disabled / inputs are degenerate.
 * Out params populate the diagnostics — pass NULL to ignore. */
static int payload_select(const Config *cfg, const RadioState *radio,
                          long target_bitrate_kbps,
                          float *out_safe_pps, float *out_required_pps,
                          bool *out_insufficient)
{
	if (out_safe_pps)      *out_safe_pps = 0.0f;
	if (out_required_pps)  *out_required_pps = 0.0f;
	if (out_insufficient)  *out_insufficient = false;

	if (cfg->fec.payload_max <= 0) return 0;
	if (!radio->valid || radio->phy_mbps <= 0.0f) return 0;
	if (target_bitrate_kbps <= 0) return 0;

	int ceiling = cfg->fec.payload_max;
	int floor_  = cfg->fec.payload_min;
	if (floor_ < 576)  floor_ = 576;
	if (floor_ > 4000) floor_ = 4000;
	if (ceiling > 4000) ceiling = 4000;
	if (ceiling < floor_) ceiling = floor_;

	float pps_at_ceiling = safe_pps(ceiling, radio->phy_mbps,
	                                cfg->fec.pkt_overhead_us,
	                                cfg->fec.pkt_overhead_bytes,
	                                cfg->fec.airtime_util);
	float bps = (float)target_bitrate_kbps * 1000.0f;
	float required_at_ceiling = bps / 8.0f / (float)ceiling;

	if (out_safe_pps)     *out_safe_pps = pps_at_ceiling;
	if (out_required_pps) *out_required_pps = required_at_ceiling;
	if (out_insufficient) *out_insufficient = (required_at_ceiling > pps_at_ceiling);

	/* Closed form: required_pps and safe_pps both decrease monotonically
	 * with P, but their ratio is bounded as P grows; the *largest*
	 * payload that fits airtime is therefore always P_max when the link
	 * permits it, and P_max is also the lowest-pps fallback when it
	 * doesn't. So one branch suffices. */
	return ceiling;
}

static void bitrate_assert(const RadioState *radio, Controller *ctrl,
                           const Config *cfg, long *last_written_kbps,
                           int *last_written_payload, uint64_t now)
{
	if (!cfg->fec.enabled) return;
	if (!radio->valid || !ctrl->have_current) return;

	float k = (float)ctrl->current.k;
	float n = (float)ctrl->current.n;
	float post_fec_kbps = radio->phy_mbps * 1000.0f * (k / n);
	long safe_kbps = (long)(post_fec_kbps * cfg->fec.safety_margin);

	long target = safe_kbps;
	if (cfg->fec.bitrate_max_kbps > 0 && target > cfg->fec.bitrate_max_kbps)
		target = cfg->fec.bitrate_max_kbps;
	if (target < cfg->fec.bitrate_min_kbps)
		target = cfg->fec.bitrate_min_kbps;

	long ref = (*last_written_kbps > 0) ? *last_written_kbps : 0;
	long dev = ref - target;
	long adev = dev < 0 ? -dev : dev;
	long tol = (long)((double)target * (double)cfg->fec.bitrate_tolerance);
	if (tol < 1) tol = 1;

	float sel_safe_pps = 0.0f, sel_req_pps = 0.0f;
	bool  sel_insufficient = false;
	int   target_payload = payload_select(cfg, radio, target,
	                                      &sel_safe_pps, &sel_req_pps,
	                                      &sel_insufficient);

	bool bitrate_changed = (*last_written_kbps < 0 || adev > tol);
	bool payload_changed = (target_payload > 0 &&
	                        target_payload != *last_written_payload);

	LOGV_FEC(cfg, "bitrate: target=%ld last_written=%ld safe=%ld min=%ld max=%ld (phy=%.1f k/n=%d/%d) payload=%d req_pps=%.0f safe_pps=%.0f",
	     target, *last_written_kbps, safe_kbps,
	     cfg->fec.bitrate_min_kbps, cfg->fec.bitrate_max_kbps,
	     radio->phy_mbps, ctrl->current.k, ctrl->current.n,
	     target_payload, (double)sel_req_pps, (double)sel_safe_pps);

	if (bitrate_changed || payload_changed) {
		const char *dir = (*last_written_kbps < 0)
		    ? "init"
		    : ((ref > target) ? "down" : "up");
		LOG_FEC("bitrate %s %ld -> %ld kbps (phy=%.1f Mbps, k/n=%d/%d, safe=%ld) payload=%d/%d%s",
		    dir, *last_written_kbps, target, radio->phy_mbps,
		    ctrl->current.k, ctrl->current.n, safe_kbps,
		    target_payload, *last_written_payload,
		    sel_insufficient ? " LINK_INSUFFICIENT_FOR_TARGET_BITRATE" : "");
		if (!cfg->dry_run) {
			/* Atomic write: when payload sizing is enabled the bitrate
			 * and payload travel in one HTTP GET to venc so the next
			 * encoded frame uses a consistent pair. */
			long bitrate_arg = bitrate_changed ? target : -1;
			int  payload_arg = payload_changed ? target_payload : -1;
			if (venc_apply(cfg, bitrate_arg, payload_arg) != 0) {
				LOG_FEC("venc apply failed (bitrate=%ld payload=%d)",
				    bitrate_arg, payload_arg);
			} else {
				if (bitrate_changed) *last_written_kbps = target;
				if (payload_changed) *last_written_payload = target_payload;
			}
		} else {
			/* Advance both in dry-run so we don't re-log the "init"
			 * transition every tick. There is no external-change
			 * detection on either field (unlike fec_k/n), so the
			 * ghost-write here doesn't corrupt operator-visible state. */
			if (bitrate_changed) *last_written_kbps = target;
			if (payload_changed) *last_written_payload = target_payload;
		}
		if (cfg->fec.bitrate_grace_s > 0.0f) {
			uint64_t new_end =
			    now + (uint64_t)(cfg->fec.bitrate_grace_s * 1e6f);
			if (new_end > ctrl->bitrate_grace_until_us)
				ctrl->bitrate_grace_until_us = new_end;
		}
	}
}

static int wfb_stats_handle_datagram(const char *body,
                                     RadioState *radio, Controller *ctrl,
                                     const Config *cfg,
                                     int *last_written_fec_k,
                                     int *last_written_fec_n,
                                     long *last_written_kbps,
                                     int *last_written_payload,
                                     long *last_seq,
                                     uint64_t now)
{
	if (!strstr(body, "\"tx_stats\"")) return -1;

	long mcs, bw, gi, stbc, ldpc, vht_mode, vht_nss;
	if (json_get_int(body, "mcs",      &mcs)      != 0) return -1;
	if (json_get_int(body, "bw",       &bw)       != 0) return -1;
	if (json_get_int(body, "short_gi", &gi)       != 0) return -1;
	if (json_get_int(body, "stbc",     &stbc)     != 0) return -1;
	if (json_get_int(body, "ldpc",     &ldpc)     != 0) return -1;
	if (json_get_int(body, "vht_mode", &vht_mode) != 0) return -1;
	if (json_get_int(body, "vht_nss",  &vht_nss)  != 0) return -1;

	long fec_k = -1, fec_n = -1;
	(void)json_get_int(body, "fec_k", &fec_k);
	(void)json_get_int(body, "fec_n", &fec_n);

	radio_apply_observation(radio, ctrl, cfg,
	                        (int)mcs, (int)bw, (int)gi,
	                        (int)stbc, (int)ldpc,
	                        (int)vht_mode, (int)vht_nss,
	                        now, false);
	radio_track_external_fec((int)fec_k, (int)fec_n,
	                         last_written_fec_k, last_written_fec_n);
	bitrate_assert(radio, ctrl, cfg, last_written_kbps,
	               last_written_payload, now);

	long drops = 0, trunc = 0;
	(void)json_get_int(body, "pkts_drop", &drops);
	(void)json_get_int(body, "pkts_trunc", &trunc);
	if (drops > 0 || trunc > 0) {
		LOG_FEC("wfb-stats: tx dropped %ld pkts (trunc=%ld) last interval",
		    drops, trunc);
	}

	long seq = -1;
	if (json_get_int(body, "seq", &seq) == 0 && seq >= 0) {
		if (*last_seq >= 0) {
			if (seq < *last_seq) {
				LOG_FEC("wfb-stats: seq went backwards (%ld -> %ld) — wfb_tx likely restarted",
				    *last_seq, seq);
			} else if (seq > *last_seq + 1) {
				LOG_FEC("wfb-stats: seq gap %ld -> %ld (%ld datagram(s) lost on the wire)",
				    *last_seq, seq, seq - *last_seq - 1);
			}
		}
		*last_seq = seq;
	}
	return 0;
}

/* venc HTTP client (tiny, blocking). */
static int http_get(const char *host, uint16_t port, const char *path,
                    char *out, size_t out_sz, int timeout_ms)
{
	struct sockaddr_in dst;
	int fd = socket(AF_INET, SOCK_STREAM, 0);
	if (fd < 0) return -1;
	memset(&dst, 0, sizeof(dst));
	dst.sin_family = AF_INET;
	dst.sin_port = htons(port);
	if (inet_pton(AF_INET, host, &dst.sin_addr) != 1) { close(fd); return -1; }

	struct timeval tv = { .tv_sec = timeout_ms / 1000,
	                      .tv_usec = (timeout_ms % 1000) * 1000 };
	setsockopt(fd, SOL_SOCKET, SO_SNDTIMEO, &tv, sizeof(tv));
	setsockopt(fd, SOL_SOCKET, SO_RCVTIMEO, &tv, sizeof(tv));

	if (connect(fd, (struct sockaddr*)&dst, sizeof(dst)) < 0) {
		close(fd); return -1;
	}

	char req[512];
	int rl = snprintf(req, sizeof(req),
	                  "GET %s HTTP/1.0\r\n"
	                  "Host: %s:%u\r\n"
	                  "User-Agent: link_controller/0.1\r\n"
	                  "Connection: close\r\n\r\n",
	                  path, host, (unsigned)port);
	if (rl <= 0 || rl >= (int)sizeof(req)) { close(fd); return -1; }
	if (send(fd, req, (size_t)rl, 0) != rl) { close(fd); return -1; }

	char buf[2048];
	size_t total = 0;
	for (;;) {
		if (total >= sizeof(buf) - 1) break;
		ssize_t n = recv(fd, buf + total, sizeof(buf) - 1 - total, 0);
		if (n <= 0) break;
		total += (size_t)n;
	}
	close(fd);
	if (total == 0) return -1;
	buf[total] = '\0';

	char *body = strstr(buf, "\r\n\r\n");
	if (!body) return -1;
	body += 4;
	size_t body_len = total - (size_t)(body - buf);
	if (body_len >= out_sz) body_len = out_sz - 1;
	memcpy(out, body, body_len);
	out[body_len] = '\0';
	return (int)body_len;
}

/* Single-shot HTTP write to venc /api/v1/set. Either or both of the
 * fields can be set; passing <=0 omits the field. When both are set
 * venc applies them atomically (LIVE_GROUP_MAX_PAYLOAD with rollback,
 * see waybeam_venc#67) so the next encoded frame uses the matching
 * bitrate / payload pair. Returns 0 on `"ok":true`, -1 otherwise. */
static int venc_apply(const Config *cfg, long bitrate_kbps, int payload_bytes)
{
	char path[160];
	int n;
	if (bitrate_kbps > 0 && payload_bytes > 0) {
		if (bitrate_kbps > 200000) bitrate_kbps = 200000;
		n = snprintf(path, sizeof(path),
		             "/api/v1/set?video0.bitrate=%ld&outgoing.maxPayloadSize=%d",
		             bitrate_kbps, payload_bytes);
	} else if (bitrate_kbps > 0) {
		if (bitrate_kbps > 200000) bitrate_kbps = 200000;
		n = snprintf(path, sizeof(path),
		             "/api/v1/set?video0.bitrate=%ld", bitrate_kbps);
	} else if (payload_bytes > 0) {
		n = snprintf(path, sizeof(path),
		             "/api/v1/set?outgoing.maxPayloadSize=%d", payload_bytes);
	} else {
		return -1;
	}
	if (n <= 0 || n >= (int)sizeof(path)) return -1;

	char body[1024];
	int got = http_get(cfg->fec.venc_host, cfg->fec.venc_port,
	                   path, body, sizeof(body), 500);
	if (got <= 0) return -1;
	return (strstr(body, "\"ok\":true") || strstr(body, "\"ok\": true")) ? 0 : -1;
}

static int sidecar_open_and_bind(void)
{
	int fd = socket(AF_INET, SOCK_DGRAM, 0);
	if (fd < 0) return -1;
	struct sockaddr_in a = { .sin_family = AF_INET,
	                         .sin_addr.s_addr = htonl(INADDR_ANY),
	                         .sin_port = 0 };
	if (bind(fd, (struct sockaddr*)&a, sizeof(a)) < 0) {
		close(fd);
		return -1;
	}
	return fd;
}

/* ── wfb_tx control (shared sender id) ──────────────────────────────── */

static uint32_t g_req_id = 1;

static int wfb_send_set_fec(const Config *cfg, int k, int n)
{
	struct sockaddr_in dst;
	if (resolve_ipv4(cfg->wfb_host, cfg->wfb_port, &dst) != 0) return -1;
	int fd = socket(AF_INET, SOCK_DGRAM, 0);
	if (fd < 0) return -1;

	CmdReq req = {0};
	req.req_id = htonl(g_req_id++);
	req.cmd_id = CMD_SET_FEC;
	req.u.set_fec.k = (uint8_t)k;
	req.u.set_fec.n = (uint8_t)n;

	ssize_t s = sendto(fd, &req, CMD_REQ_SET_FEC_LEN, 0,
	                   (const struct sockaddr*)&dst, sizeof(dst));
	close(fd);
	return (s == CMD_REQ_SET_FEC_LEN) ? 0 : -1;
}

static int wfb_get_radio(const Config *cfg, RadioBody *out)
{
	struct sockaddr_in dst;
	if (resolve_ipv4(cfg->wfb_host, cfg->wfb_port, &dst) != 0) return -1;
	int fd = socket(AF_INET, SOCK_DGRAM, 0);
	if (fd < 0) return -1;

	uint32_t req_id = g_req_id++;
	CmdReq req = {0};
	req.req_id = htonl(req_id);
	req.cmd_id = CMD_GET_RADIO;

	if (sendto(fd, &req, CMD_REQ_GET_RADIO_LEN, 0,
	           (const struct sockaddr*)&dst, sizeof(dst)) != CMD_REQ_GET_RADIO_LEN) {
		close(fd);
		return -1;
	}

	struct pollfd p = { .fd = fd, .events = POLLIN };
	int pr = poll(&p, 1, 300);
	if (pr <= 0) { close(fd); return -1; }

	CmdResp resp = {0};
	ssize_t n = recv(fd, &resp, sizeof(resp), 0);
	close(fd);
	if (n < (ssize_t)(sizeof(uint32_t) * 2 + sizeof(RadioBody))) return -1;
	if (ntohl(resp.req_id) != req_id) return -1;
	if (ntohl(resp.rc) != 0) return -1;

	*out = resp.u.get_radio;
	return 0;
}

static int wfb_set_radio(const Config *cfg, const RadioBody *params)
{
	struct sockaddr_in dst;
	if (resolve_ipv4(cfg->wfb_host, cfg->wfb_port, &dst) != 0) return -1;
	int fd = socket(AF_INET, SOCK_DGRAM, 0);
	if (fd < 0) return -1;

	uint32_t req_id = g_req_id++;
	CmdReq req = {0};
	req.req_id = htonl(req_id);
	req.cmd_id = CMD_SET_RADIO;
	req.u.set_radio = *params;
	req.u.set_radio.ldpc     = req.u.set_radio.ldpc     ? 1 : 0;
	req.u.set_radio.short_gi = req.u.set_radio.short_gi ? 1 : 0;
	req.u.set_radio.vht_mode = req.u.set_radio.vht_mode ? 1 : 0;

	if (sendto(fd, &req, CMD_REQ_SET_RADIO_LEN, 0,
	           (const struct sockaddr*)&dst, sizeof(dst)) != CMD_REQ_SET_RADIO_LEN) {
		close(fd);
		return -1;
	}

	struct pollfd p = { .fd = fd, .events = POLLIN };
	int pr = poll(&p, 1, 300);
	if (pr <= 0) { close(fd); return -1; }

	CmdResp resp = {0};
	ssize_t n = recv(fd, &resp, sizeof(resp), 0);
	close(fd);
	if (n < (ssize_t)(sizeof(uint32_t) * 2)) return -1;
	if (ntohl(resp.req_id) != req_id) return -1;
	if (ntohl(resp.rc) != 0) return -1;
	return 0;
}

